package cn.ac.sklois.imm.mappings;

public class MeasurementValues {
	
	private String HashValues;
	private String SoftwareName;
	private String Edition;
	private String Manufacturer;
	private String IssueTime;
	private String Description;
	private String CopyRight;
	private String SoftwareClass;
	
	public String getHashValues() {
		return HashValues;
	}
	public void setHashValues(String hashValues) {
		HashValues = hashValues;
	}
	public String getSoftwareName() {
		return SoftwareName;
	}
	public void setSoftwareName(String softwareName) {
		SoftwareName = softwareName;
	}
	public String getEdition() {
		return Edition;
	}
	public void setEdition(String edition) {
		Edition = edition;
	}
	public String getManufacturer() {
		return Manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		Manufacturer = manufacturer;
	}
	public String getIssueTime() {
		return IssueTime;
	}
	public void setIssueTime(String issueTime) {
		IssueTime = issueTime;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public String getCopyRight() {
		return CopyRight;
	}
	public void setCopyRight(String copyRight) {
		CopyRight = copyRight;
	}
	public String getSoftwareClass() {
		return SoftwareClass;
	}
	public void setSoftwareClass(String softwareClass) {
		SoftwareClass = softwareClass;
	}
	
	
	
}
