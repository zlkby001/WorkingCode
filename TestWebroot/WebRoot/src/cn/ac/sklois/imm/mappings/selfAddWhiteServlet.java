	/**
	 * The doPost method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to post.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		String filename=new String(request.getParameter("filename").getBytes("ISO-8859-1"),"UTF-8");
		String digest=request.getParameter("shash");
		boolean b=false;
		MappingService ms=new MappingService();
		UserBean a = (UserBean)request.getSession(true).getAttribute("a");
		int userid = Integer.parseInt(a.getID());
		b=ms.addWhitebyfilenameandhash(filename,digest,userid);
		if(b)
			request.setAttribute("result","添加白名单成功!");
		else
			request.setAttribute("result","添加白名单失败!");
		request.getRequestDispatcher("../selfAddWhiteResult.jsp").forward(request,response);
		
	}

