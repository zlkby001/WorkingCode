	/**
	 * The doGet method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		String oidstr=request.getParameter("oid");
		String flag=request.getParameter("flag");
		int oid=Integer.parseInt(oidstr);
		SuperAdminService sas=new SuperAdminService();
		boolean b=sas.DeleteAdmin(oid);
		if(b){
			
			 audit.log_record(oid, loginservlet.usertype, loginservlet.name1, "审核新帐户", "拒绝");
			request.setAttribute("result","删除管理员成功!");
			
		}else request.setAttribute("result","删除管理员失败!");
		
		if(flag.equalsIgnoreCase("unpass")){
			request.getRequestDispatcher("../deleteAdminResult.jsp?flag=unpass").forward(request,response);
		}else{
			request.getRequestDispatcher("../deleteAdminResult.jsp?flag=pass").forward(request,response);
		}
		
		
	}

